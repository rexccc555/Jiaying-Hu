export function getPoisByRegion(regionId: string): PoiTemplate[] {
  if (regionId === NZ_OPEN_REGION_ID) {
    return POIS.slice(0, 28);
  }
  return POIS.filter((p) => p.regionId === regionId);
}

export function getPoiById(id: string): PoiTemplate | undefined {
  return POIS.find((p) => p.id === id);
}
