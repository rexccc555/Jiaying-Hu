export function getRegionById(id: string): Region | undefined {
  return REGIONS.find((r) => r.id === id);
}

/** 仅返回当前已有 POI 模板的区域（避免向导出现空目录） */
export function getRegionsWithPois(): Region[] {
  return REGIONS.filter(
    (r) => r.id !== NZ_OPEN_REGION_ID && getPoisByRegion(r.id).length > 0,
  );
}
